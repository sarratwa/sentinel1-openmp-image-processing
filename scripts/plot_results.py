from pathlib import Path

import matplotlib.pyplot as plt
import pandas as pd


def main() -> None:
    csv_path = Path("results/benchmark_results.csv")
    results_dir = Path("results")

    if not csv_path.exists():
        print(f"Error: CSV file not found: {csv_path}")
        print("Run the C program first to generate benchmark_results.csv")
        return

    results_dir.mkdir(parents=True, exist_ok=True)

    df = pd.read_csv(csv_path)

    required_columns = {
        "filter",
        "version",
        "benchmark_type",
        "threads",
        "schedule",
        "runtime",
        "speedup",
        "efficiency",
    }

    missing_columns = required_columns.difference(df.columns)

    if missing_columns:
        print(
            "Error: CSV is missing these columns:",
            ", ".join(sorted(missing_columns)),
        )
        print(
            "If this CSV was generated by an older version of main.c, "
            "re-run the benchmark to regenerate it with the current schema."
        )
        return

    print("Loaded benchmark results:")
    print(df)

    # ------------------------------------------------------------
    # Thread scaling plots (schedule=static, threads varying)
    # ------------------------------------------------------------
    thread_df = df[df["benchmark_type"] == "thread_scaling"].copy()

    if thread_df.empty:
        print("Warning: No thread_scaling rows found in CSV. Skipping thread-scaling plots.")
    else:
        thread_df = thread_df.sort_values("threads")

        plt.figure()
        plt.plot(thread_df["threads"], thread_df["runtime"], marker="o")
        plt.xlabel("Number of Threads")
        plt.ylabel("Runtime in Seconds")
        plt.title("OpenMP Gaussian Filter: Runtime vs Threads")
        plt.grid(True)
        plt.xticks(thread_df["threads"])
        plt.tight_layout()
        plt.savefig(results_dir / "runtime_vs_threads.png", dpi=300, bbox_inches="tight")
        plt.close()

        plt.figure()
        plt.plot(thread_df["threads"], thread_df["speedup"], marker="o")
        plt.xlabel("Number of Threads")
        plt.ylabel("Speedup")
        plt.title("OpenMP Gaussian Filter: Speedup vs Threads")
        plt.grid(True)
        plt.xticks(thread_df["threads"])
        plt.tight_layout()
        plt.savefig(results_dir / "speedup_vs_threads.png", dpi=300, bbox_inches="tight")
        plt.close()

        plt.figure()
        plt.plot(thread_df["threads"], thread_df["efficiency"], marker="o")
        plt.xlabel("Number of Threads")
        plt.ylabel("Efficiency")
        plt.title("OpenMP Gaussian Filter: Efficiency vs Threads")
        plt.grid(True)
        plt.xticks(thread_df["threads"])
        plt.tight_layout()
        plt.savefig(results_dir / "efficiency_vs_threads.png", dpi=300, bbox_inches="tight")
        plt.close()

        print("\nThread-scaling plots saved:")
        print(results_dir / "runtime_vs_threads.png")
        print(results_dir / "speedup_vs_threads.png")
        print(results_dir / "efficiency_vs_threads.png")

    # ------------------------------------------------------------
    # Scheduling comparison plots (fixed threads, schedule varying)
    # ------------------------------------------------------------
    schedule_df = df[df["benchmark_type"] == "schedule_comparison"].copy()

    if schedule_df.empty:
        print("Warning: No schedule_comparison rows found in CSV. Skipping scheduling plots.")
    else:
        # Preserve a fixed, readable order rather than alphabetical sorting
        schedule_order = ["static", "dynamic", "guided"]
        schedule_df["schedule"] = pd.Categorical(
            schedule_df["schedule"], categories=schedule_order, ordered=True
        )
        schedule_df = schedule_df.sort_values("schedule")

        threads_used = schedule_df["threads"].iloc[0]

        # Runtime by schedule
        plt.figure()
        plt.bar(schedule_df["schedule"].astype(str), schedule_df["runtime"])
        plt.xlabel("Scheduling Strategy")
        plt.ylabel("Runtime in Seconds")
        plt.title(f"OpenMP Gaussian Filter: Runtime by Schedule ({threads_used} threads)")
        plt.grid(True, axis="y")
        plt.tight_layout()
        plt.savefig(results_dir / "runtime_vs_schedule.png", dpi=300, bbox_inches="tight")
        plt.close()

        # Speedup by schedule
        plt.figure()
        plt.bar(schedule_df["schedule"].astype(str), schedule_df["speedup"])
        plt.xlabel("Scheduling Strategy")
        plt.ylabel("Speedup")
        plt.title(f"OpenMP Gaussian Filter: Speedup by Schedule ({threads_used} threads)")
        plt.grid(True, axis="y")
        plt.tight_layout()
        plt.savefig(results_dir / "speedup_vs_schedule.png", dpi=300, bbox_inches="tight")
        plt.close()

        # Efficiency by schedule
        plt.figure()
        plt.bar(schedule_df["schedule"].astype(str), schedule_df["efficiency"])
        plt.xlabel("Scheduling Strategy")
        plt.ylabel("Efficiency")
        plt.title(f"OpenMP Gaussian Filter: Efficiency by Schedule ({threads_used} threads)")
        plt.grid(True, axis="y")
        plt.tight_layout()
        plt.savefig(results_dir / "efficiency_vs_schedule.png", dpi=300, bbox_inches="tight")
        plt.close()

        print("\nScheduling comparison plots saved:")
        print(results_dir / "runtime_vs_schedule.png")
        print(results_dir / "speedup_vs_schedule.png")
        print(results_dir / "efficiency_vs_schedule.png")


if __name__ == "__main__":
    main()